#!/usr/bin/env node
/**
 * Writes supabase-config.js from .env (no hand-editing the browser file).
 * Usage: npm run sup
 */
import { readFileSync, writeFileSync, existsSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const envPath = join(root, ".env");

const DEFAULT_REF = "wxcuipdfrohozowdhlql";

function parseEnv(text) {
  const out = {};
  text.split(/\r?\n/).forEach(function (line) {
    line = line.trim();
    if (!line || line[0] === "#") return;
    var m = /^export\s+/.test(line) ? line.replace(/^export\s+/, "") : line;
    var eq = m.indexOf("=");
    if (eq === -1) return;
    var k = m.slice(0, eq).trim();
    var v = m.slice(eq + 1).trim();
    if ((v[0] === '"' && v[v.length - 1] === '"') || (v[0] === "'" && v[v.length - 1] === "'")) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  });
  return out;
}

function main() {
  var fileEnv = {};
  if (existsSync(envPath)) {
    fileEnv = parseEnv(readFileSync(envPath, "utf8"));
  }

  var env = Object.assign({}, fileEnv);
  if (process.env.SUPABASE_ANON_KEY) env.SUPABASE_ANON_KEY = process.env.SUPABASE_ANON_KEY;
  if (process.env.SUPABASE_URL) env.SUPABASE_URL = process.env.SUPABASE_URL;
  if (process.env.SUPABASE_PROJECT_REF) env.SUPABASE_PROJECT_REF = process.env.SUPABASE_PROJECT_REF;
  if (process.env.VITE_SUPABASE_ANON_KEY) env.VITE_SUPABASE_ANON_KEY = process.env.VITE_SUPABASE_ANON_KEY;

  var anon = String(env.SUPABASE_ANON_KEY || env.VITE_SUPABASE_ANON_KEY || "").trim();
  var url = String(env.SUPABASE_URL || "").trim();

  if (!anon && !existsSync(envPath)) {
    console.error(
      "Missing .env — copy .env.example to .env and set SUPABASE_ANON_KEY, then run: npm run sup\n" +
        "Or: SUPABASE_ANON_KEY=eyJ... npm run sup"
    );
    process.exit(1);
  }
  if (!url) {
    var ref = String(env.SUPABASE_PROJECT_REF || DEFAULT_REF).trim();
    url = "https://" + ref + ".supabase.co";
  }

  if (!anon) {
    console.error(
      "Set SUPABASE_ANON_KEY in .env or export it in the shell (Project Settings → API → anon public)."
    );
    process.exit(1);
  }

  var js =
    "/**\n * Generated by: npm run sup (scripts/sync-supabase-config.mjs)\n * Do not edit by hand; change .env and re-run npm run sup\n */\n" +
    "window.SONGSHARE_SUPABASE = " +
    JSON.stringify({ url: url, anonKey: anon }, null, 2) +
    ";\n\n" +
    "(function () {\n" +
    "  var c = window.SONGSHARE_SUPABASE || {};\n" +
    "  var k = String(c.anonKey || \"\").trim();\n" +
    "  var k2 = String(window.SONGSHARE_SUPABASE_ANON_KEY || \"\").trim();\n" +
    "  if (c.url && !k && !k2 && typeof console !== \"undefined\" && console.warn) {\n" +
    "    console.warn(\n" +
    "      \"[Noteion] Add your Supabase anon public key — run npm run sup after setting .env\"\n" +
    "    );\n" +
    "  }\n" +
    "  if ((k || k2).indexOf(\"http\") === 0 && typeof console !== \"undefined\" && console.error) {\n" +
    "    console.error(\"[Noteion] The anon key must be a JWT (eyJ…), not a URL.\");\n" +
    "  }\n" +
    "})();\n";

  var outFile = join(root, "supabase-config.js");
  writeFileSync(outFile, js, "utf8");
  console.log("Wrote " + outFile);
}

main();
